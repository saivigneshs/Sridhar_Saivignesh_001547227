    private void btnCalcAgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcAgeActionPerformed
         try {
            // TODO add your handling code here:
            Date currentDate = new Date(System.currentTimeMillis());
            Date birthDate=new SimpleDateFormat("MM/dd/yyyy").parse(nullValHandle(FtxtDob.getText()));
            DateFormat dformat = new SimpleDateFormat("yyyyMMdd");                           
            int inBirthDate = Integer.parseInt(dformat.format(birthDate));                            
            int inCurrDate = Integer.parseInt(dformat.format(currentDate));                          
            int age = (inCurrDate - inBirthDate) / 10000;          
            txtAge.setText(String.valueOf(age));
            person.setInAge(age);
         } catch (ParseException ex) {
            Logger.getLogger(DemographPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCalcAgeActionPerformed
    private String nullValHandle(String strVal) {
        if (strVal == null )
          return ""; 
     return strVal.trim();
  }

