    private void displayAccDetails() {
        
        try{
        Account checkAccount = person.getCheckAccount();
        Account saveAccount = person.getSaveAccount();
      
        txtCheckBankName.setText(checkAccount.getStBankName());
        txtCheckAccNo.setText(checkAccount.getStAccNo());
        txtCheckAccBal.setText(String.valueOf(checkAccount.getDbAccBal()));
        txtCheckRoutNo.setText(checkAccount.getStRoutNo());
        
        txtSaveBankName.setText(saveAccount.getStBankName());
        txtSaveAccNo.setText(saveAccount.getStAccNo());
        txtSaveAccBal.setText(String.valueOf(saveAccount.getDbAccBal()));
        txtSaveRoutNo.setText(saveAccount.getStRoutNo());
        }catch(Exception e){
        JOptionPane.showMessageDialog(this, "Kindly check if the Account Details are properly Submitted.","Warning : Missing Information",2);
        }
    }
