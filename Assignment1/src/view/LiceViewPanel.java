    private void displayLicense() {
        try{
       txtLiceNo.setText(person.getLicense().getStLicNo());
       txtDOI.setText(person.getLicense().getStIssDate());
       txtDOE.setText(person.getLicense().getStExpDate());
       txtBloodType.setText(person.getLicense().getStBloodType());
       Image imPhotoView = person.getLicense().getImPhoto();
       ImageIcon imIconView = new ImageIcon(imPhotoView);
       lblPhotoView.setIcon(imIconView);
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(this, "Kindly check if the License Details are properly Submitted.","Warning : Missing Information",2);
        }
    }
