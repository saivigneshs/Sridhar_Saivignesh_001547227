    private void generateReport() throws ParseException {
    try{    
        //Personal Details
        repName.setText(person.getStLastName()+", "+person.getStFirstName());
        repAge.setText(person.getInAge()+"yrs. old.");
        repSSN.setText(person.getStSSN());
        String mobNo = person.getStMobNo();
        if(! (mobNo == null) ){
        repMobileNo.setText("("+mobNo.substring(0,3)+")-"+mobNo.substring(3,6)+"-"+mobNo.substring(6,10));
        }
        repHeight.setText(person.getInHeight()+" cms.");
        repWeight.setText(person.getInWeight()+" kgs.");
        repDOB.setText(person.getStDOB());
        
        //Address Details
        repStreetAddr.setText(person.getAddress().getStStreetName());
        repAddr.setText(person.getAddress().getStCity()+", "+person.getAddress().getStState()
                +" - "+person.getAddress().getStZipCode());
        
        //Medical Details
        repMedRecNo.setText(person.getMedical().getStMedRecNo());
        repAller1.setText(person.getMedical().getStAllergy1());
        repAller2.setText(person.getMedical().getStAllergy2());
        repAller3.setText(person.getMedical().getStAllergy3());
        
        //License Details
        repLicNo.setText(person.getLicense().getStLicNo());
        repDOI.setText(person.getLicense().getStIssDate());
        repDOE.setText(person.getLicense().getStExpDate());
        repBloodGroup.setText(person.getLicense().getStBloodType());
        
            SimpleDateFormat sdf  = new SimpleDateFormat("MM/dd/yyyy"); 
            Date DOI = sdf.parse(person.getLicense().getStIssDate()); 
            Date DOE = sdf.parse(person.getLicense().getStExpDate());  
            long timeDiff = DOE.getTime() - DOI.getTime(); 
            Calendar cal = Calendar.getInstance(); 
            cal.setTimeInMillis(timeDiff);
            int yearDiff = cal.get(Calendar.YEAR)-1970;
            int monthDiff = cal.get(Calendar.MONTH); 
            int dayDiff = cal.get(Calendar.DAY_OF_MONTH)-1;
                
        repLiceDets.setText(yearDiff+" years, "+monthDiff+" months, "+dayDiff+" days");
        Image imPhotoView = person.getLicense().getImPhoto();
        ImageIcon imIconView = new ImageIcon(imPhotoView);
        repPhoto.setIcon(imIconView);
        
        //Bank Details - Populated based on the Radio button selection
    } catch(Exception e){
        JOptionPane.showMessageDialog(this, "Kindly check if ALL the Details are properly Submitted.","Warning : Missing Information",2);
    } 
    }
